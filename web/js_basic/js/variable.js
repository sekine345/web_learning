// 変数

// 変数 x を宣言
var x;

// 複数の変数 i, j を宣言
// var i;
// var j;
var i,j;

// 宣言時に初期値を設定する
var str = 'はじめてのJavaScript';
var k = 100;

// 命名規則
// １文字目は英字 or アンダースコア or $記号
var name;
var _name;
var $name;

// ２文字目は１文字目に使える文字 + 数字

var foo;

// 例題
// 変数colorName を宣言すると同時に、'blue'を設定
// 変数colorName の値をコンソールに出力
var colorName = 'blue';
